        private void SwitchSource_Clicked(object sender, EventArgs e)
        {
            var timer = new Stopwatch();
            timer.Start();

            ProductShowListview.ItemsSource = ProductActivities.ProductLoad();

            timer.Stop();

            TimeSpan timeTaken = timer.Elapsed;
            string foo = timeTaken.ToString(@"m\:ss\.fff");
            var toast = Toast.Make("Time to load data from db: " + foo, CommunityToolkit.Maui.Core.ToastDuration.Long, 14);
            toast.Show();
        }
